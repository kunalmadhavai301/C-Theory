#include <iostream> 
using  namespace  std ; 
int  main () { 
int  age =  20 ;  
float  height =  5.9 ;  
// 'age' is an identifier 
// 'height' is an identifier 
cout  <<  "Age: "  << age <<  endl ; 
cout  <<  "Height: "  << height <<  endl ; 
return  0 ;  
// 'main' and 'return' are keywords, not identifiers
}