#include <iostream> 
using  namespace  std ; 
int  main () { 
int  a =  30 , b =  40, c =  15 ; 

cout  <<  "Logical AND (a > b && b < c): "  << (a  > b && b < c) 
<<  endl ; 
 
cout  <<  "Logical OR (a < b || b < c): "  << (a  < b || b < c) 
<<  endl ; 

cout  <<  "Logical NOT (!(a > b)): "  << !(a > b)  <<  endl ; 
return  0 ; 
}
