#include <iostream>
using namespace std;

 int main() {
 int age = 45; // Integer
 
 float height = 4.35; // Float
 
 char grade = 'v'; // Character
 
 bool isStudent = false; // Boolean
 
 double pi = 3.141592659; // Double
 
 cout << "Age: " << age << endl;
 cout << "Height: " << height << " feet" << endl;
 cout << "Grade: " << grade << endl;
 cout << "Is Student: " << isStudent << endl;
 cout << "Value of Pi: " << pi << endl;
 
 return 0;
}
